char	*ft_strrchr(const char *str, int c)
{
	int	i;

	i = 0;
	while (str)
	{
		str++;
		i++;
	}
	while (i > 0)
	{
		if (*str == c)
			return ((char *)str);
		str--;
		i--;
	}
	return (0);
}
